    // -- Verifications

         private Boolean verifyCustomerData(){
         // -- Le sous formulaire customer doit etre rempli
        Boolean retour = true;
        retour &= verifyField(jTextFirstName) & verifyField(jTextLastName);
        retour &= verifyField(jTextLogin) & verifyField(jTextPassword);
        retour &= verifyField(jTextMail);
        if(retour == true){
            // -- Tous les champs sont remplis, on vérifie leurs contenu
            // -- Mail
            if (!isEmailAdress(jTextMail.getText())){
                retour = false;
                jTextMail.setBorder(BorderFactory.createLineBorder( Color.red ));
                jTextMail.setText("Bad format - use username@host.xx");
            }
            else
                jTextMail.setBorder(BorderFactory.createLineBorder( Color.black ));
        }
        return retour;
    }
     private Boolean verifyCustomerCard(){
         Boolean retour = true;
         Date today = new Date();
         if(!calendarExpCardDate.getDate().after(today)){
            calendarExpCardDate.setBorder(BorderFactory.createLineBorder(Color.RED));
            retour &= false;
         }else {
             calendarExpCardDate.setBorder(BorderFactory.createLineBorder(Color.GREEN));
         }
         retour &= verifyField(jTextCardNumber);
         return retour;
     }
