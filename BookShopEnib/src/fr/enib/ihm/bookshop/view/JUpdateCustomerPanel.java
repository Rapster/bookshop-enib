    private void jButtonUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonUpdateMouseClicked
        if(verifyCustomerData() & verifyCustomerCard()){
            // -- Définition de l'adresse
            int streetNumber = 0;
            if(!jTextStreetNumber.getText().isEmpty())  streetNumber = Integer.valueOf(jTextStreetNumber.getText());
            Address customerAddress = new Address( streetNumber, jTextStreet.getText(),
                                              jTextCity.getText(), jTextZcode.getText(), jTextCountry.getText());
            
            Customer customer = new Customer( jTextLogin.getText(),  jTextFirstName.getText(),  jTextLastName.getText(),
                                                 jTextTelephone.getText(),  jTextMail.getText(),  customerAddress);
            customer.setId(customerToUpdated.getId());
            customer.setPassword(jTextPassword.getText());
            customer.setDateOfBirth(calendarBirthDate.getDate());
            // -- Creditcard
            CreditCard customerCard = new CreditCard();
            customerCard.setCreditCardNumber(jTextCardNumber.getText());
            customerCard.setCreditCardType(jTextCardType.getText());
            SimpleDateFormat d = new SimpleDateFormat ("dd/MM/yyyy" );
            String dateString = d.format(this.calendarExpCardDate.getDate());
            customerCard.setCreditCardExpDate(dateString);
            customer.setCreditCard(customerCard);
               // -- Tirage d'évenement
               ApplicationEventsDispatcher.getApplicationEventsDispatcher().fireUpdateCustomerEvent(new CustomerEvent(evt, customer));
               this.getParent().remove(this); // Close panel
        }
}//GEN-LAST:event_jButtonUpdateMouseClicked

